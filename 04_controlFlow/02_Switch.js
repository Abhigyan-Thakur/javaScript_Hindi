const number = 10;
switch (number) {
    case 1: {
        console.log("January");
        break;
    }
    case 2: {
        console.log("feb")
        break;
    }
    case 3: {
        console.log("march")
        break;
    }
    case 4: {
        console.log("april")
        break;
    }
    case 5: {
        console.log("may")
        break;
    }
    default: {
        console.log("Default value the input is not described");
        break;
    }
}